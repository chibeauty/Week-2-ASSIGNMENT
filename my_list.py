# 1. Created an empty list
my_list = []

# 2. Appended elements to the list
my_list.append(10)
my_list.append(20)
my_list.append(30)
my_list.append(40)

# 3. Inserted the value 15 at the second position in the list.
my_list.insert(1, 15)

# 4. Extended the list with another list
my_list.extend([50, 60, 70])

# 5. Removed the last element from the list
my_list.pop()

# 6. Sorted my_list in ascending order
my_list.sort()

# 7. Find and print the index of the value 30 in my_list.
print(my_list.index(30))